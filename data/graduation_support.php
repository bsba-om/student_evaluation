/**
 * Graduation schema, curriculum completion checks, disk paths, and FPDF prospectus output.
 */

declare(strict_types=1);

function graduation_pdf_text(string $s): string
{
    $t = @iconv('UTF-8', 'Windows-1252//TRANSLIT', $s);
    return $t !== false ? $t : preg_replace('/[^\x20-\x7E]/', '?', $s);
}

function graduation_major_slug(string $displayName): string
{
    $n = strtolower(trim($displayName));
    if (str_contains($n, 'operational')) {
        return 'om';
    }
    if (str_contains($n, 'financial')) {
        return 'fm';
    }
    if (str_contains($n, 'marketing')) {
        return 'mm';
    }
    $slug = preg_replace('/[^a-z0-9]+/', '', $n);

    return $slug !== '' ? substr($slug, 0, 14) : 'major';
}

function graduation_student_file_slug(string $first, string $last, int $studentId = 0): string
{
    $a = strtolower(preg_replace('/[^a-z0-9]+/i', '_', trim($first)));
    $b = strtolower(preg_replace('/[^a-z0-9]+/i', '_', trim($last)));
    $name = trim($a . '_' . $b, '_');
    if ($name === '') $name = 'student';
    $sid = $studentId > 0 ? 's' . $studentId : '';
    return $name . ($sid ? '_' . $sid : '');
}

function graduation_disk_base(): string
{
    return 'C:/graduate/';
}

function graduation_batch_folder(string $batchYear): string
{
    return 'batch ' . $batchYear;
}

function graduation_major_full_name(string $slug): string
{
    $n = strtolower($slug);
    if (str_contains($n, 'op') || str_contains($n, 'om'))  return 'Operational Management';
    if (str_contains($n, 'fn') || str_contains($n, 'fm'))  return 'Financial Management';
    if (str_contains($n, 'mk') || str_contains($n, 'mm'))  return 'Marketing Management';
    return $slug;
}

/**
 * Parse filename-based graduate record into structured fields.
 *
 * Filename convention (generated by graduation_generate_prospectus_pdf):
 *   {last}_{first}_s{studentId}_{majorSlug}_gwa{value}_batch{YYYY-YYYY}.pdf
 *   {first}_{last}_s{studentId}_{majorSlug}_gwa{value}_batch{YYYY-YYYY}.pdf
 * Example 1: wadawdaw_opop_s231313_fm_gwa1.23_batch2025-2026.pdf
 * Example 2: opop_wadawdaw_s1234_om_gwa1.55_batch2024-2025.pdf
 *
  * Strategy: split on "_" → classify each token by VALUE → collect named tokens.
  */
function parse_graduate_pdf(string $relPath, string $baseDir): array
{
    $file   = basename($relPath);
    $stem   = pathinfo($file, PATHINFO_FILENAME);   // strip .pdf
    $t      = array_values(array_filter(explode('_', $stem)));  // clean token list

    $batchYear  = '';
    $gwaStr     = '';
    $studentId  = '';
    $majorSlug  = '';
    $reserved   = [];   // token indices to skip (batch / gwa / s{id} / major)

    // ── classify by VALUE, not position ──────────────────────────────────────
    // Known value tokens skip ANYWHERE in the array so they never get confused
    // with name tokens.
    foreach ($t as $i => $tok) {
        if ($tok === '') continue;

        // batch year: exactly "batchYYYY-YYYY"
        if (preg_match('/^batch\d{4}-\d{4}$/', $tok)) {
            if (preg_match('/^batch(\d{4}-\d{4})$/', $tok, $bm)) $batchYear = $bm[1];
            $reserved[] = $i;
            continue;
        }

        // gwa value: "gwa{value}" — float value at end
        if (preg_match('/^gwa(\d+(?:\.\d+)?)$/', $tok, $gm)) {
            $gwaStr = $gm[1];
            $reserved[] = $i;
            continue;
        }

        // student id token: starts with 's' then all digits, e.g. "s231313" "s42"
        // Also catch plain-dropped student ids without leading "s"
        if ($tok[0] === 's' && isset($tok[1]) && ctype_digit(substr($tok, 1))) {
            $studentId  = substr($tok, 1);
            $reserved[] = $i;
            continue;
        }

        if (ctype_digit($tok) && strlen($tok) >= 4) {
            $studentId  = $tok;
            $reserved[] = $i;
            continue;
        }
    }

    // ── majorSlug — always the token RIGHT AFTER _s{id} (known structure) ───────
    //    After classification: [nameTokens..., s{id}?, majorSlug?, gwa?, batch?]
    //    The item immediately after (first occurrence of) _s{digits} is always
    //    the major code when present.
    $sidPos = null;
    foreach ($t as $idx => $tok) {
        if (preg_match('/^s?\d+$/', $tok) && strlen($tok) >= 2 && ctype_digit(substr($tok, ($tok[0]==='s'?1:0)))) {
            $sidPos = $idx;
            break;
        }
    }
    if ($sidPos !== null && isset($t[$sidPos + 1])) {
        $tok = $t[$sidPos + 1];
        if (preg_match('/^[a-z]{2,10}$/', $tok)) $majorSlug = $tok;
    }

    // ── Fallback: old 3-token format last_first_slug where no s{id} was embedded ──
    //    Token set = [last/1st, 1st/last, majorSlug, batchYear] (no gwa, no s{id}).
    //    Major is the last alphabetic slug token. No need to strip from $t since
    //    nameTokens section below skips tokens that were in $reserved.
    if ($majorSlug === '') {
        // Walk from RIGHT-1 (skip last which is always batchYear) to the left
        for ($ri = count($t) - 2; $ri >= 0; $ri--) {
            if (in_array($ri, $reserved, true)) continue;
            $tok = $t[$ri];
            // Accept 2-10 alpha slug codes; exclude anything that was a pure all-digit token
            if (preg_match('/^[a-z]{2,10}$/', $tok)) {
                $majorSlug = $tok;
                $reserved[] = $ri;
                break;
            }
        }
    }

    // ── remaining = name tokens in original order ────────────────────────────
    // Convention in the filename: first undropped token = first_name token comes
    // first alphabetically / shortest nickname; last token = last_name.
    // Based on observed data: filename has "_" joining tokens and the pattern
    // for the name part ALWAYS starts with the one that comes first in order.
    // We reconstruct by:
    //  - everything up to (but not including) the first name-token → ignored
    //  - first name-token = known to be <=3 alpha chars (first name)
    //  - last name-token = next (or only) remaining token
    // For both old format {last}_{first} and new format {first}_{last}_s{id}_{major}:
    //   after stripping non-name tokens the remaining array is [nameA, nameB].
    //   convention: nameB = first_name, nameA = last_name (alphabetical order)
    //   (e.g. doe_john → nameA='doe'=last, nameB='john'=first)

    $nameTokens = [];
    foreach ($t as $i => $tok) {
        if (!in_array($i, $reserved, true)) $nameTokens[] = $tok;
    }

    // If no reserved tokens were found (very old file or no tokens),
    // treat entire array as name tokens.
    if (empty($reserved)) $nameTokens = $t;

    // Convention: last_name = first non-name word in the NAME part of filename,
    // first_name = second word. Filename: {last}_{first}_{optional_more_suffixes}.
    // After stripping suffixes we're left with [nameA, nameB].
    //   doe_john_om_batch.YYYY.YYYY → ['doe','john'] → last=doe, first=john ✓
    //   opop_wadawdaw_om_batch.YYYY.YYYY → ['opop','wadawdaw'] → last=opop, first=wadawdaw
    if (count($nameTokens) >= 2) {
        $last  = $nameTokens[0];                                           // surname
        $first = $nameTokens[1];                                           // given name
    } elseif (count($nameTokens) === 1) {
        $last  = $nameTokens[0];
        $first = '';
    } else {
        $last = $first = '';
    }

    $majorName    = graduation_major_full_name($majorSlug);
    $studentLabel = trim(($last ?: $first) . ', ' . ($first ?: ''));

    return [
        'file_path'     => $relPath,
        'file_name'     => $file,
        'batch_year'    => $batchYear,
        'major_slug'    => $majorSlug,
        'major_name'    => $majorName,
        'first_name'    => $first,
        'last_name'     => $last,
        'student_label' => $studentLabel,
        'student_number'=> $studentId !== '' ? $studentId : null,
        'gwa'           => $gwaStr !== '' ? (float) $gwaStr : null,
    ];
}

/**
 * Directory shape (as produced by graduation_generate_prospectus_pdf):
 *   C:/graduate/
 *     batch 2025-2026/
 *       om/    last_first_s{studentId}_om_gwa{value}_batch2025-2026.pdf
 *       fm/    last_first_s{studentId}_fm_gwa{value}_batch2025-2026.pdf
 *       mm/    ...
 *     batch 2024-2025/
 *       ...
 */
function scan_graduate_pdfs(string $baseDir = 'C:/graduate/'): array
{
    $records = [];
    if (!is_dir($baseDir)) return $records;

    $batchDirs = @scandir($baseDir);
    if ($batchDirs === false) return $records;

    foreach ($batchDirs as $batchDir) {
        if ($batchDir === '.' || $batchDir === '..') continue;
        $batchPath = $baseDir . $batchDir;
        if (!is_dir($batchPath)) continue;
        if (!preg_match('/batch\s+(\d{4}-\d{4})/', $batchDir, $bm)) continue;
        $batchYear = $bm[1];

        $majorDirs = @scandir($batchPath);
        if ($majorDirs === false) continue;

        foreach ($majorDirs as $majorDir) {
            if ($majorDir === '.' || $majorDir === '..') continue;
            $majorPath = $batchPath . '/' . $majorDir;
            if (!is_dir($majorPath)) continue;

            $files = @scandir($majorPath);
            if ($files === false) continue;

            foreach ($files as $file) {
                if ($file === '.' || $file === '..') continue;
                if (strtolower(pathinfo($file, PATHINFO_EXTENSION)) !== 'pdf') continue;

                $relPath  = $majorPath . '/' . $file;
                $rec      = parse_graduate_pdf($file, $majorPath);

                // ── layer sidecar JSON if present ─────────────────────────────────
                $metaPath = $relPath . '.json';
                if (is_file($metaPath)) {
                    $metaJson = @file_get_contents($metaPath);
                    if ($metaJson !== false) {
                        $meta = json_decode($metaJson, true);
                        if (is_array($meta)) {
                            $rec['gwa']            = $meta['gwa']            ?? $rec['gwa'];
                            $rec['graduation_date'] = $meta['graduation_date'] ?? null;
                            $rec['total_subjects']  = $meta['total_subjects']  ?? 0;
                            $rec['units_passed']    = $meta['units_passed']    ?? null;
                            $rec['student_name']    = $meta['student_name']    ?? null;
                            $rec['student_number']  = $meta['student_number']  ?? $rec['student_number'];
                            $rec['document_title']  = $meta['document_title']  ?? null;
                        }
                    }
                }

                $rec['file_path'] = $relPath;
                $records[] = $rec;
            }
        }
    }

    return $records;
}

function ensure_graduation_schema(PDO $pdo): void
{
    try {
        $pdo->exec("ALTER TABLE students MODIFY COLUMN status ENUM('regular','transfer','non_ibm','graduated') DEFAULT 'regular'");
    } catch (Throwable $e) {
        // already migrated or insufficient privilege
    }

    $pdo->exec("CREATE TABLE IF NOT EXISTS graduation_records (
        id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
        student_id INT NOT NULL,
        major_id INT NOT NULL,
        academic_year VARCHAR(32) NOT NULL DEFAULT '',
        year_level VARCHAR(64) NOT NULL DEFAULT '',
        semester VARCHAR(64) NOT NULL DEFAULT '',
        gwa DECIMAL(10,4) DEFAULT NULL,
        graduation_date DATE DEFAULT NULL,
        total_subjects INT UNSIGNED DEFAULT 0,
        pdf_path VARCHAR(768) DEFAULT NULL,
        created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
        updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
        UNIQUE KEY uq_grad_student (student_id)
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci");

    try {
        $pdo->exec('ALTER TABLE graduation_records ADD COLUMN pdf_path VARCHAR(768) DEFAULT NULL');
    } catch (Throwable $e) {
    }
}

function student_graduation_locked(PDO $pdo, int $studentId): bool
{
    $st = $pdo->prepare('SELECT status FROM students WHERE id = ? LIMIT 1');
    $st->execute([$studentId]);
    $row = $st->fetch(PDO::FETCH_ASSOC);
    if ($row && ($row['status'] ?? '') === 'graduated') {
        return true;
    }
    $gr = $pdo->prepare('SELECT id FROM graduation_records WHERE student_id = ? LIMIT 1');
    $gr->execute([$studentId]);

    return (bool) $gr->fetchColumn();
}

/**
 * Latest grade_rounded per subject_id for a student (by graded_at, then id).
 *
 * @return array<int,float|null>
 */
function graduation_latest_grade_map(PDO $pdo, int $studentId): array
{
    $stmt = $pdo->prepare('SELECT subject_id, grade_rounded, graded_at, id FROM student_grades WHERE student_id = ? ORDER BY graded_at DESC, id DESC');
    $stmt->execute([$studentId]);
    $map = [];
    foreach ($stmt->fetchAll(PDO::FETCH_ASSOC) as $g) {
        $sid = (int) $g['subject_id'];
        if (!array_key_exists($sid, $map)) {
            $map[$sid] = $g['grade_rounded'] !== null ? (float) $g['grade_rounded'] : null;
        }
    }

    return $map;
}

/**
 * @return array{
 *   total_required:int,
 *   passed:int,
 *   pending:int,
 *   blocked:int,
 *   complete:bool,
 *   gwa:?float,
 *   units_total:float,
 *   units_passed:float
 * }
 */
function student_curriculum_completion(PDO $pdo, int $studentId, int $majorId): array
{
    $stmt = $pdo->prepare('
        SELECT s.id AS subject_id, s.units
        FROM major_subjects ms
        INNER JOIN subjects s ON s.id = ms.subject_id
        WHERE ms.major_id = ? AND ms.is_required = 1
    ');
    $stmt->execute([$majorId]);
    $rows = $stmt->fetchAll(PDO::FETCH_ASSOC);
    $gradeMap = graduation_latest_grade_map($pdo, $studentId);

    $totalReq = count($rows);
    $pending = 0;
    $blocked = 0;
    $passed = 0;
    $unitsTotal = 0.0;
    $unitsPassed = 0.0;
    $points = 0.0;

    foreach ($rows as $r) {
        $sid = (int) $r['subject_id'];
        $units = (float) $r['units'];
        $unitsTotal += $units;
        $gr = $gradeMap[$sid] ?? null;
        if ($gr === null) {
            $pending++;

            continue;
        }
        if ($gr <= 3.0) {
            $passed++;
            $unitsPassed += $units;
            $points += $gr * $units;
        } else {
            $blocked++;
        }
    }

    $complete = $totalReq > 0 && $pending === 0 && $blocked === 0 && $passed === $totalReq;
    $gwa = ($complete && $unitsPassed > 0) ? round($points / $unitsPassed, 4) : null;

    return [
        'total_required' => $totalReq,
        'passed' => $passed,
        'pending' => $pending,
        'blocked' => $blocked,
        'complete' => $complete,
        'gwa' => $gwa,
        'units_total' => round($unitsTotal, 2),
        'units_passed' => round($unitsPassed, 2),
    ];
}

function graduation_load_ph_settings(PDO $pdo): array
{
    try {
        $stmt = $pdo->prepare("SELECT setting_value FROM settings WHERE setting_key = 'program_head_settings'");
        $stmt->execute();
        $row = $stmt->fetch(PDO::FETCH_ASSOC);
        if ($row && $row['setting_value']) {
            return json_decode($row['setting_value'], true) ?: [];
        }
    } catch (Throwable $e) {
    }

    return [];
}

/**
 * @return array{0:string,1:string}
 */
function graduation_advisor_and_program_head(PDO $pdo, int $instructorId): array
{
    $advisorName = '';
    try {
        $stmtAdv = $pdo->prepare('SELECT first_name, middle_name, last_name, suffix FROM instructors WHERE id = ?');
        $stmtAdv->execute([$instructorId]);
        $advRow = $stmtAdv->fetch(PDO::FETCH_ASSOC);
        if ($advRow) {
            $advisorName = trim($advRow['first_name'] . ($advRow['middle_name'] ? ' ' . substr((string) $advRow['middle_name'], 0, 1) . '.' : '') . ' ' . $advRow['last_name'] . ($advRow['suffix'] ? ' ' . $advRow['suffix'] : ''));
        }
    } catch (Throwable $e) {
    }

    $programHeadName = '';
    try {
        $stmtPH = $pdo->query("
            SELECT i.first_name, i.middle_name, i.last_name, i.suffix
            FROM instructors i
            LEFT JOIN admin_promotions ap ON i.id = ap.instructor_id
            WHERE ap.promoted_to = 'program_head'
            ORDER BY ap.promotion_date DESC
            LIMIT 1
        ");
        if ($stmtPH && ($phRow = $stmtPH->fetch(PDO::FETCH_ASSOC))) {
            $programHeadName = trim($phRow['first_name'] . ($phRow['middle_name'] ? ' ' . substr((string) $phRow['middle_name'], 0, 1) . '.' : '') . ' ' . $phRow['last_name'] . ($phRow['suffix'] ? ' ' . $phRow['suffix'] : ''));
        }
    } catch (Throwable $e) {
    }

    return [$advisorName, $programHeadName];
}

function graduation_pdf_grade_status(?float $g): string
{
    if ($g === null) {
        return '—';
    }
    if ($g <= 3.0) {
        return 'Passed';
    }
    if ($g < 5.0) {
        return 'Conditional';
    }

    return 'Failed';
}

/**
 * Mark graduated, save record, write prospectus PDF (print-style). Returns payload for JSON.
 *
 * @return array<string,mixed>
 */
function graduation_complete_workflow(
    PDO $pdo,
    int $instructorId,
    int $studentId,
    int $majorIdPost,
    string $academicYear,
    string $yearLevel,
    string $semester
): array {
    $pdfPath = null;
    try {
        ensure_graduation_schema($pdo);

        $stmt = $pdo->prepare('
            SELECT s.*, m.display_name AS major_name
            FROM students s
            JOIN mentees me ON s.id = me.student_id AND me.mentor_id = ?
            LEFT JOIN majors m ON s.major_id = m.id
            WHERE s.id = ?
            LIMIT 1
        ');
        $stmt->execute([$instructorId, $studentId]);
        $student = $stmt->fetch(PDO::FETCH_ASSOC);
        if (!$student) {
            return ['success' => false, 'message' => 'Student not found or access denied'];
        }

        if (student_graduation_locked($pdo, $studentId)) {
            return ['success' => false, 'message' => 'This student is already marked as graduated.'];
        }

        $major_id = (int) ($student['major_id'] ?? 0);
        if ($major_id <= 0) {
            return ['success' => false, 'message' => 'Invalid major for this student.'];
        }
        if ($majorIdPost > 0 && $major_id !== $majorIdPost) {
            return ['success' => false, 'message' => 'Invalid major for this student.'];
        }

        $curriculum = student_curriculum_completion($pdo, $studentId, $major_id);
        if (!$curriculum['complete']) {
            return [
                'success' => false,
                'message' => 'Curriculum is not fully complete. Resolve pending or failed subjects before graduation.',
                'curriculum' => $curriculum,
            ];
        }

        $gwa = (float) ($curriculum['gwa'] ?? 0);
        $totalSubjects = (int) $curriculum['total_required'];
        [$advisorName, $programHeadName] = graduation_advisor_and_program_head($pdo, $instructorId);
        $phSettings = graduation_load_ph_settings($pdo);

        $graduationDate = date('Y-m-d');
        $pdfErr = null;
        $pdfPath = graduation_generate_prospectus_pdf(
            $pdo,
            $student,
            $academicYear,
            $advisorName,
            $programHeadName,
            $phSettings,
            $gwa,
            $graduationDate,
            $pdfErr
        );

        if (!$pdfPath) {
            return ['success' => false, 'message' => 'PDF could not be generated: ' . ($pdfErr ?: 'unknown error')];
        }

        $pdo->beginTransaction();

        $pdo->prepare('UPDATE students SET status = ?, year_level = ?, updated_at = NOW() WHERE id = ?')
            ->execute(['graduated', '4th Year - 2nd Semester', $studentId]);

        $pdo->prepare('
            INSERT INTO graduation_records
                (student_id, major_id, academic_year, year_level, semester, gwa, graduation_date, total_subjects, pdf_path)
            VALUES (?,?,?,?,?,?,?,?,?)
            ON DUPLICATE KEY UPDATE
                major_id = VALUES(major_id),
                academic_year = VALUES(academic_year),
                year_level = VALUES(year_level),
                semester = VALUES(semester),
                gwa = VALUES(gwa),
                graduation_date = VALUES(graduation_date),
                total_subjects = VALUES(total_subjects),
                pdf_path = VALUES(pdf_path),
                updated_at = NOW()
        ')->execute([
            $studentId,
            $major_id,
            $academicYear,
            $yearLevel,
            $semester,
            $gwa,
            $graduationDate,
            $totalSubjects,
            $pdfPath,
        ]);

        $pdo->commit();

        $pdfUrl = '../../../data/download_graduation_pdf.php?student_id=' . $studentId;

        // Build the folder path for display
        $base       = rtrim(graduation_disk_base(), '\/');
        $folderPath = $base . DIRECTORY_SEPARATOR . graduation_batch_folder($academicYear)
                    . DIRECTORY_SEPARATOR . graduation_major_slug((string)($student['major_name'] ?? ''));

        return [
            'success'          => true,
            'message'          => 'Student graduated. Official prospectus PDF saved under C:\\graduate\\…',
            'total_subjects'   => $totalSubjects,
            'gwa'              => round($gwa, 4),
            'graduation_date'  => $graduationDate,
            'pdf_url'          => $pdfUrl,
            'pdf_path'         => $pdfPath,
            'folder_path'      => $folderPath,
        ];
    } catch (Throwable $e) {
        if ($pdo->inTransaction()) {
            $pdo->rollBack();
        }
        if (!empty($pdfPath) && is_file($pdfPath)) {
            @unlink($pdfPath);
        }

        return ['success' => false, 'message' => $e->getMessage()];
    }
}

/**
 * Graduation-only PDF re-generation — used when the original file is missing.
 * Differs from graduation_complete_workflow in three important ways:
 *   1. Does NOT set $pdo->inTransaction() guard or rollback
 *   2. Does NOT touch students.status / year_level
 *   3. Skips the student_graduation_locked() check
 *
 * Only touches graduation_records.pdf_path (UPSERT) and writes the file disk.
 *
 * @return array{success:bool, pdf_url?:string, pdf_path?:string, message:string}
 */
function graduation_regenerate_pdf_only(
    PDO $pdo,
    int $instructorId,
    int $studentId,
    string $academicYear,
    string $yearLevel,
    string $semester
): array {
    $pdfErr = null;

    // ── 1. Fetch student (verify instructor owns this student) ─────────────
    $stmt = $pdo->prepare('
        SELECT s.*, m.display_name AS major_name
          FROM students s
          JOIN mentees me ON s.id = me.student_id AND me.mentor_id = ?
          LEFT JOIN majors m ON s.major_id = m.id
         WHERE s.id = ?
         LIMIT 1
    ');
    $stmt->execute([$instructorId, $studentId]);
    $student = $stmt->fetch(PDO::FETCH_ASSOC);
    if (!$student) {
        return ['success' => false, 'message' => 'Student not found or access denied'];
    }

    $major_id = (int) ($student['major_id'] ?? 0);
    if ($major_id <= 0) {
        return ['success' => false, 'message' => 'Student has no major assigned.'];
    }

    // ── 2. Curriculum and GWA ─────────────────────────────────────────────
    $curriculum = student_curriculum_completion($pdo, $studentId, $major_id);
    if (!$curriculum['complete']) {
        return [
            'success' => false,
            'message' => 'Curriculum is not fully complete. Cannot regenerate prospectus for an incomplete student.',
            'curriculum' => $curriculum,
        ];
    }

    $gwa            = (float) ($curriculum['gwa'] ?? 0);
    $totalSubjects  = (int) $curriculum['total_required'];

    // ── 3. Advisor / Program-Head ─────────────────────────────────────────
    [$advisorName, $programHeadName] = graduation_advisor_and_program_head($pdo, $instructorId);
    $phSettings = graduation_load_ph_settings($pdo);

    // ── 4. Generate the PDF ────────────────────────────────────────────────
    $pdfPath = graduation_generate_prospectus_pdf(
        $pdo, $student, $academicYear,
        $advisorName, $programHeadName, $phSettings,
        $gwa, date('Y-m-d'), $pdfErr
    );
    if (!$pdfPath) {
        return ['success' => false, 'message' => 'PDF could not be generated: ' . ($pdfErr ?: 'unknown error')];
    }

    // ── 5. UPSERT graduation_records (pdf_path only — leave other fields as-is) ──
    $pdo->prepare('
        INSERT INTO graduation_records
            (student_id, major_id, academic_year, year_level, semester, gwa, graduation_date, total_subjects, pdf_path)
        VALUES (?,?,?,?,?,?,?,?,?)
        ON DUPLICATE KEY UPDATE
            major_id      = VALUES(major_id),
            academic_year = VALUES(academic_year),
            year_level    = VALUES(year_level),
            semester      = VALUES(semester),
            pdf_path      = VALUES(pdf_path),
            updated_at    = NOW()
    ')->execute([
        $studentId,
        $major_id,
        $academicYear,
        $yearLevel,
        $semester,
        $gwa,
        date('Y-m-d'),
        $totalSubjects,
        $pdfPath,
    ]);

    return [
        'success'         => true,
        'message'         => 'Prospectus PDF regenerated successfully.',
        'total_subjects'  => $totalSubjects,
        'gwa'             => round($gwa, 4),
        'graduation_date' => date('Y-m-d'),
        'pdf_url'         => '../../../data/download_graduation_pdf.php?student_id=' . $studentId,
        'pdf_path'        => $pdfPath,
    ];
}

function graduation_fetch_record(PDO $pdo, int $studentId): ?array
{
    $st = $pdo->prepare('SELECT * FROM graduation_records WHERE student_id = ? LIMIT 1');
    $st->execute([$studentId]);
    $row = $st->fetch(PDO::FETCH_ASSOC);

    return $row ?: null;
}

function graduation_build_fs_path(string $batchYear, string $majorSlug, string $filename): string
{
    $base     = rtrim(graduation_disk_base(), '\/');
    $batchDir = $base . DIRECTORY_SEPARATOR . graduation_batch_folder($batchYear);
    $majorDir = $batchDir . DIRECTORY_SEPARATOR . $majorSlug;
    $dir      = $majorDir . DIRECTORY_SEPARATOR;

    // Create each level separately for clear error messages
    foreach ([$base, $batchDir, $majorDir] as $segment) {
        if (!is_dir($segment)) {
            if (!@mkdir($segment, 0777, false) && !is_dir($segment)) {
                throw new RuntimeException(
                    'Could not create graduation directory: ' . $segment .
                    ' — check that PHP has write permission to ' . $base
                );
            }
        }
    }

    return $dir . $filename;
}

/**
 * Print-style official prospectus PDF (matches on-screen / print grade report layout).
 * Saves under C:/graduate/batch {AY}/{om|fm|mm}/firstname_lastname_major_batch{AY}.pdf
 */
function graduation_generate_prospectus_pdf(
    PDO $pdo,
    array $student,
    string $academicYear,
    string $advisorName,
    string $programHeadName,
    array $phSettings,
    float $gwa,
    string $graduationDateYmd,
    ?string &$errorOut
): ?string {
    $errorOut = null;
    $lib = __DIR__ . '/lib/fpdf186/fpdf.php';
    $fontPath = __DIR__ . '/lib/fpdf186/font/';
    if (!is_file($lib)) {
        $errorOut = 'FPDF library missing (data/lib/fpdf186/fpdf.php)';

        return null;
    }
    if (!defined('FPDF_FONTPATH')) {
        define('FPDF_FONTPATH', $fontPath);
    }
    if (!is_dir($fontPath) || !is_file($fontPath . 'helveticai.php')) {
        $errorOut = 'FPDF font directory is missing or incomplete: ' . $fontPath;
        return null;
    }
    require_once $lib;

    $majorName = (string) ($student['major_name'] ?? '');
    $majorSlug = graduation_major_slug($majorName);
    $studentId = (int) ($student['id'] ?? 0);
    $slug      = graduation_student_file_slug((string) ($student['first_name'] ?? ''), (string) ($student['last_name'] ?? ''), $studentId);
    $gwaPart   = '_gwa' . number_format((float) $gwa, 2, '.', '');
    $filename  = $slug . '_' . $majorSlug . $gwaPart . '_batch' . $academicYear . '.pdf';
    $path      = graduation_build_fs_path($academicYear, $majorSlug, $filename);

    $majorId = (int) ($student['major_id'] ?? 0);

    $stmt = $pdo->prepare('
        SELECT s.id AS subject_id, s.subject_code, s.subject_name, s.units, ms.year_level, ms.semester
        FROM major_subjects ms
        INNER JOIN subjects s ON s.id = ms.subject_id
        WHERE ms.major_id = ? AND ms.is_required = 1
        ORDER BY FIELD(ms.year_level,\'Bridging\',\'1st Year\',\'2nd Year\',\'3rd Year\',\'4th Year\'),
                 FIELD(ms.semester,\'1st Semester\',\'2nd Semester\'),
                 ms.sort_order
    ');
    $stmt->execute([$majorId]);
    $subjects = $stmt->fetchAll(PDO::FETCH_ASSOC);
    $gradeMap = graduation_latest_grade_map($pdo, $studentId);

    $school = $phSettings['school_name'] ?? 'Northern Bukidnon State College';
    $addr = $phSettings['school_address'] ?? '';
    $inst = $phSettings['institute_name'] ?? '';
    $degree = $phSettings['degree_name'] ?? '';

    $gdTs = strtotime($graduationDateYmd);
    $gdPretty = $gdTs ? date('F j, Y', $gdTs) : $graduationDateYmd;

    $pdf = new FPDF('P', 'mm', 'Letter');
    $pdf->SetMargins(14, 14, 14);
    $pdf->SetAutoPageBreak(true, 18);
    $pdf->AddPage();

    $pdf->SetDrawColor(139, 105, 20);
    $pdf->SetLineWidth(0.4);
    $pdf->Line(14, $pdf->GetY(), 202, $pdf->GetY());
    $pdf->Ln(2);

    $pdf->SetFont('Helvetica', 'B', 13);
    $pdf->SetTextColor(60, 40, 10);
    $pdf->Cell(0, 7, graduation_pdf_text($school), 0, 1, 'C');
    $pdf->SetFont('Helvetica', 'I', 9);
    $pdf->SetTextColor(80, 80, 80);
    if ($addr !== '') {
        $pdf->Cell(0, 5, graduation_pdf_text($addr), 0, 1, 'C');
    }
    $pdf->SetFont('Helvetica', 'B', 10);
    $pdf->SetTextColor(60, 40, 10);
    if ($inst !== '') {
        $pdf->Cell(0, 5, graduation_pdf_text($inst), 0, 1, 'C');
    }
    if ($degree !== '') {
        $pdf->SetFont('Helvetica', '', 9);
        $pdf->Cell(0, 5, graduation_pdf_text($degree), 0, 1, 'C');
    }
    $pdf->SetFont('Helvetica', 'B', 11);
    $pdf->SetTextColor(139, 105, 20);
    $pdf->Cell(0, 6, 'Major in ' . graduation_pdf_text($majorName), 0, 1, 'C');
    $pdf->Ln(1);
    $pdf->SetFont('Helvetica', 'B', 10);
    $pdf->Cell(0, 6, 'Student Evaluation Prospectus (Official Copy)', 0, 1, 'C');
    $pdf->Ln(3);

    // ════════════════════════════════════════════════════════════════════════
    // Student info strip — mirrors the HTML student-info-strip-print block
    // (Student name · Student No · Year Level · Semester)
    // ════════════════════════════════════════════════════════════════════════
    $siY  = $pdf->GetY();
    $siH  = 12.5;
    $siW  = 188;
    $siCw = [$siW / 4, $siW / 4, $siW / 4, $siW / 4];   // equal quarters
    $pdf->SetFillColor(248, 246, 240);
    $pdf->SetDrawColor(184, 134, 11);
    $pdf->SetLineWidth(0.2);
    $pdf->Rect(14, $siY, $siW, $siH, 'DF');

    $fullName = trim(($student['last_name'] ?? '') . ', ' . ($student['first_name'] ?? '') . ' ' . ($student['middle_name'] ?? ''));
    list($yrNum, $semNum) = [1, 1];
    $standingStr = (string) ($student['year_level'] ?? '');
    if (preg_match('/(\d+)(st|nd|rd|th)\s*Year/i', $standingStr, $m))  $yrNum  = (int)$m[1];
    if (preg_match('/(\d+)(st|nd|rd|th)\s*Sem/i',  $standingStr, $m2)) $semNum = (int)$m2[1];
    $yearLabels = ['1st Year','2nd Year','3rd Year','4th Year'];
    $semLabels  = ['1st Semester','2nd Semester'];
    $dispYL = $yearLabels[$yrNum - 1] ?? $standingStr;
    $dispSL = $semLabels[$semNum - 1]  ?? $semNum . 'th Semester';
    $siItems = [
        ['Student',      esc($fullName)],
        ['Student No.',  esc((string)($student['student_id'] ?? $student['student_number'] ?? '—'))],
        ['Year Level',   esc($dispYL)],
        ['Semester',     esc($dispSL)],
    ];
    foreach ($siItems as $idx => [$lbl, $val]) {
        $x = 14 + array_sum(array_slice($siCw, 0, $idx));
        $pdf->SetXY($x + 1, $siY + 1.5);
        $pdf->SetFont('Helvetica', '', 7.5);
        $pdf->SetTextColor(130, 100, 50);
        $pdf->Cell($siCw[$idx] - 1.5, 3.5, $lbl . ':');
        $pdf->SetXY($x + 1, $siY + 1.5 + 3.5);
        $pdf->SetFont('Helvetica', 'B', 8.5);
        $pdf->SetTextColor(30, 25, 15);
        $pdf->Cell($siCw[$idx] - 1.5, 4.5, $val);
    }
    $pdf->SetTextColor(0, 0, 0);
    $pdf->SetDrawColor(200, 200, 200);
    $pdf->SetLineWidth(0.15);
    $pdf->SetY($siY + $siH + 2);

    // thin gold accent line under student-info strip
    $pdf->SetDrawColor(139, 105, 20);
    $pdf->SetLineWidth(0.3);
    $pdf->Line(14, $pdf->GetY(), 202, $pdf->GetY());
    $pdf->SetLineWidth(0.15);
    $pdf->Ln(2);

    $pdf->SetDrawColor(200, 200, 200);
    $pdf->SetLineWidth(0.15);
    $pdf->SetFillColor(250, 248, 244);
    $pdf->SetTextColor(0, 0, 0);
    $boxY = $pdf->GetY();
    $pdf->Rect(14, $boxY, 188, 28, 'DF');
    $pdf->SetXY(18, $boxY + 3);
    $pdf->SetFont('Helvetica', '', 9);
    $fullName = trim(($student['last_name'] ?? '') . ', ' . ($student['first_name'] ?? '') . ' ' . ($student['middle_name'] ?? ''));
    $pdf->Cell(55, 5, 'Student:', 0, 0);
    $pdf->SetFont('Helvetica', 'B', 9);
    $pdf->Cell(115, 5, graduation_pdf_text($fullName), 0, 1);
    $pdf->SetX(18);
    $pdf->SetFont('Helvetica', '', 9);
    $pdf->Cell(55, 5, 'Student No.:', 0, 0);
    $pdf->Cell(115, 5, graduation_pdf_text((string) ($student['student_id'] ?? $student['student_number'] ?? '')), 0, 1);
    $pdf->SetX(18);
    $pdf->Cell(55, 5, 'Academic Year:', 0, 0);
    $pdf->Cell(55, 5, graduation_pdf_text($academicYear), 0, 0);
    $pdf->Cell(40, 5, 'Graduation date:', 0, 0);
    $pdf->SetFont('Helvetica', 'B', 9);
    $pdf->Cell(0, 5, graduation_pdf_text($gdPretty), 0, 1);
    $pdf->SetX(18);
    $pdf->SetFont('Helvetica', '', 9);
    $pdf->Cell(55, 5, 'Status:', 0, 0);
    $pdf->SetTextColor(22, 101, 52);
    $pdf->SetFont('Helvetica', 'B', 9);
    $pdf->Cell(0, 5, 'GRADUATED', 0, 1);
    $pdf->SetTextColor(0, 0, 0);
    $pdf->SetY($boxY + 30);

    $drawTableHeader = static function ($pdf): void {
        $pdf->SetFont('Helvetica', 'B', 7.5);
        $pdf->SetFillColor(184, 134, 11);
        $pdf->SetTextColor(255, 255, 255);
        $pdf->Cell(13, 6.5, 'GRADE',   1, 0, 'C', true);
        $pdf->Cell(26, 6.5, 'CODE',    1, 0, 'L', true);
        $pdf->Cell(90, 6.5, 'DESCRIPTION', 1, 0, 'L', true);
        $pdf->Cell(12, 6.5, 'UNIT',    1, 0, 'C', true);
        $pdf->Cell(47, 6.5, 'YEAR / SEMESTER', 1, 1, 'L', true);
        $pdf->SetTextColor(0, 0, 0);
    };

    $drawTableHeader($pdf);

    $pdf->SetFont('Helvetica', '', 7.5);
    $sumUnits = 0.0;
    $maxY = 252;

    // ── section-group subject rendering ─────────────────────────────────────
    $GRADE_ORDER = ['Bridging','1st Year','2nd Year','3rd Year','4th Year'];
    $SEM_ORDER   = ['1st Semester','2nd Semester'];

    // ── compute totals for JSON sidecar ──────────────────────────────────────
    $totalSubjects = count($subjects);
    $sumUnitsPassed = 0.0;

    // Attach raw grade values in readable order
    $subjectsWithGrade = array_map(function ($sub) use ($gradeMap, $GRADE_ORDER, $SEM_ORDER) {
        $sid = (int) ($sub['subject_id'] ?? 0);
        $g   = $gradeMap[$sid] ?? null;
        $sub['_grade'] = $g;
        $sub['_year_ord']  = array_search(trim((string)($sub['year_level'] ?? '')), $GRADE_ORDER);
        $sub['_sem_ord']   = array_search(trim((string)($sub['semester'] ?? '')),   $SEM_ORDER);
        if ($sub['_year_ord'] === false) $sub['_year_ord'] = 999;
        if ($sub['_sem_ord']  === false) $sub['_sem_ord']  = 999;
        return $sub;
    }, $subjects);

    // Sort by year then semester preserving sort_order within each group
    usort($subjectsWithGrade, function ($a, $b) {
        if ($a['_year_ord'] !== $b['_year_ord'])
            return $a['_year_ord'] <=> $b['_year_ord'];
        if ($a['_sem_ord'] !== $b['_sem_ord'])
            return $a['_sem_ord'] <=> $b['_sem_ord'];
        return (int)($a['sort_order'] ?? 0) <=> (int)($b['sort_order'] ?? 0);
    });

    // Build grouped sections: each entry = [header_label, [subjects...]]
    $sections = [];
    $currentKey = null;
    foreach ($subjectsWithGrade as $sub) {
        $key = trim((string)($sub['year_level'] ?? '')) . '|' . trim((string)($sub['semester'] ?? ''));
        if ($key !== $currentKey) {
            $currentKey = $key;
            $sections[] = ['header' => $key, 'subjects' => []];
        }
        $sections[count($sections) - 1]['subjects'][] = $sub;
    }

    /** @var array<int,array{margin_top:float,sub_total_units:float,sub_row_count:int} $secStats */
    $secStats = [];
    $grandCourses   = 0;
    $grandUnits     = 0.0;

    $STAMP   = "\xDE\xAD";   // replacing blank / codes with a safety-pin mark for code cells – rendered below
    $EMPTY_GRADE = '—';

    foreach ($sections as $secIdx => $sec) {
        $secList    = $sec['subjects'];
        $headerKey  = (string) $sec['header'];
        [$yr, $sem] = explode('|', $headerKey, 2);

        $subUnits = 0.0;
        foreach ($secList as $ss) { $subUnits += (float)($ss['units'] ?? 0); }

        $subCourses = count($secList);
        $grandCourses += $subCourses;
        $grandUnits   += $subUnits;

        $secStats[] = ['sub_total_units' => $subUnits, 'sub_row_count' => $subCourses];

        // ── year-semester section header — mirrors print CSS .pro-year-hdr/.pro-sem-label ──
        // Shown for every section (including the first), matching the HTML print layout that
        // pairs a black label ("1ST YEAR — FIRST SEMESTER") with the grade-table rows below it.
        if ($pdf->GetY() > $maxY - 18) { $pdf->AddPage(); $drawTableHeader($pdf); $pdf->SetFont('Helvetica','',7.5); }
        $pdf->SetFillColor(139, 105, 20);
        $pdf->SetTextColor(255, 255, 255);
        $pdf->SetFont('Helvetica', 'B', 7.5);
        $pdf->Cell(0, 5, '  ' . graduation_pdf_text(strtoupper($yr) . ' — ' . strtoupper($sem) . '  —  ' . $subCourses . ' Subject' . ($subCourses !== 1 ? 's' : '') . '  —  ' . number_format($subUnits, 1) . ' units'), 0, 1, 'L', true);
        $pdf->SetFont('Helvetica', '', 7.5);
        $pdf->SetTextColor(0, 0, 0);
        $fillFlag = false;

        // ── leading hatch mark on first row of each section ──────────────────
        $prevY    = $pdf->GetY();
        $firstRow = true;

        // ── subject rows ────────────────────────────────────────────────────
        foreach ($secList as $sub) {
            if ($pdf->GetY() > $maxY) {
                $pdf->AddPage();
                $drawTableHeader($pdf);
                $pdf->SetFont('Helvetica', '', 7.5);
            }
            $sid   = (int) ($sub['subject_id'] ?? 0);
            $g     = $sub['_grade'] ?? null;
            $gStr  = $g !== null ? number_format($g, 2) : $EMPTY_GRADE;
            $code  = graduation_pdf_text(trim((string)($sub['subject_code'] ?? '')));
            $nameRaw = graduation_pdf_text((string)($sub['subject_name'] ?? ''));
            $name  = strlen($nameRaw) > 44 ? substr($nameRaw, 0, 41).'...' : $nameRaw;
            $units = (float) $sub['units'];
            $sumUnits += $units;
            if ($g !== null && $g <= 3.0) $sumUnitsPassed += $units;
            $ys = graduation_pdf_text(trim(($sub['year_level'] ?? ''). ' / ' .($sub['semester'] ?? '')));

            // alternating band (skip for first section header lines)
            if ($pdf->GetY()>$maxY) { $pdf->SetFont('Helvetica','',7.5); }

            $fillRow = false; // let table grid alternate naturally
            $rowFill = ($fillFlag = !$fillFlag) ? [237,230,210] : [248,245,238];

            // --- corner / safety-pin mark on first row of each section -----
            if ($firstRow) {
                $markX = $pdf->GetX() + 1;
                $markY = $pdf->GetY() + 2.8;
                $pdf->SetLineWidth(0.2);
                $pdf->Line($markX + 2.5, $markY, $markX + 12, $markY);
                $pdf->Line($markX + 2.5, $markY, $markX + 2.5, $markY + 1);
                $pdf->Line($markX + 12, $markY, $markX + 12, $markY + 1);
                $pdf->SetLineWidth(0.15);
                $firstRow = false;
            }

            // ─ GRADE cell — score only, single row, no status badge ───────────
            $gradeH = 5.5;
            $pdf->Cell(13, $gradeH, $gStr ?? $EMPTY_GRADE, 1, 0, 'C');

            // ─ CODE (left-aligned, bold)
            $pdf->SetFillColor($rowFill[0],$rowFill[1],$rowFill[2]);
            $pdf->Cell(28, $gradeH, $code, 1, 0, 'L', true);

            // ─ DESCRIPTION (left-aligned, normal)
            $pdf->Cell(93, $gradeH, $name, 1, 0, 'L');

            // ─ UNITS (centered)
            $pdf->Cell(12, $gradeH, (string)number_format($units,1), 1, 0, 'C');

            // ─ YEAR/SEMESTER (left-aligned)
            $pdf->Cell(42, $gradeH, $ys, 1, 1, 'L');
        }

    }

    if ($pdf->GetY() > $maxY - 36) { $pdf->AddPage(); $drawTableHeader($pdf); $pdf->SetFont('Helvetica','',7.5); }

    // ─ grand totals row — matches print CSS .pro-total-row td ─────────────────
    //   cream fill (#f0ece0) · dark-gold text (#8B6914) · thin gold top-border
    $pdf->SetFillColor(240, 236, 224);   // #f0ece0
    $pdf->SetTextColor(139, 105, 20);     // #8B6914
    $pdf->SetFont('Helvetica', 'B', 7.5);
    $pdf->SetX(14);
    $pdf->SetDrawColor(184, 134, 11);
    $pdf->SetLineWidth(0.4);
    $pdf->Cell(188, 6.5,
        number_format($grandCourses, 0).' Subject' . ($grandCourses !== 1 ? 's' : '') . ' ·  ' .
        number_format($sumUnits, 1).' units  ' .
        str_repeat(' ', 5).'  Final GWA: '.number_format($gwa, 4),
        1, 1, 'L', true);
    $pdf->SetTextColor(0, 0, 0);

    // ─ thin gold accent line under totals ──────────────────────────────────────
    $pdf->SetDrawColor(184, 134, 11);
    $pdf->SetLineWidth(0.3);
    $pdf->Line(14, $pdf->GetY() + 1, 202, $pdf->GetY() + 1);
    $pdf->SetLineWidth(0.15);

    $pdf->Ln(10);
    $pdf->SetDrawColor(139, 105, 20);
    $pdf->Line(14, $pdf->GetY(), 202, $pdf->GetY());
    $pdf->Ln(8);
    $pdf->SetFont('Helvetica', '', 8);
    $pdf->Cell(94, 4, 'Prepared by:', 0, 0, 'L');
    $pdf->Cell(0, 4, 'Certified correct:', 0, 1, 'L');
    $pdf->Ln(10);
    $pdf->SetFont('Helvetica', 'B', 9);
    $pdf->Cell(94, 5, graduation_pdf_text($advisorName !== '' ? $advisorName : 'Academic Adviser'), 'T', 0, 'C');
    $pdf->Cell(0, 5, graduation_pdf_text($programHeadName !== '' ? $programHeadName : 'Program Head'), 'T', 1, 'C');
    $pdf->SetFont('Helvetica', '', 7);
    $pdf->Cell(94, 4, "Adviser's signature line", 0, 0, 'C');
    $pdf->Cell(0, 4, "Program Head's signature line", 0, 1, 'C');

    try {
        $pdf->Output('F', $path);
    } catch (Throwable $e) {
        $errorOut = $e->getMessage();

        return null;
    }

    // ── save JSON sidecar so the graduate list page can display structured fields ─
    try {
        $meta = [
            'student_name'     => graduation_pdf_text(trim(($student['first_name'] ?? '').' '.($student['last_name'] ?? ''))),
            'major_name'       => $majorName,
            'batch_year'       => $academicYear,
            'gwa'              => $gwa,
            'graduation_date'  => $graduationDateYmd,
            'total_subjects'   => (int) $totalSubjects,
            'units_passed'     => round($sumUnitsPassed, 2),
            'document_title'   => 'Student Evaluation Prospectus (Official Copy)',
            // Embed the student_id (user-facing, e.g. 23121321) so the scan can
            // surface the correct number in the graduate-list table without a DB hit.
            'student_number'   => (string) ($student['student_id'] ?? $student['id'] ?? ''),
        ];
        @file_put_contents($path . '.json', json_encode($meta, JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE));
    } catch (Throwable) {
        // sidecar is best-effort; PDF was saved successfully
    }

    return $path;
}